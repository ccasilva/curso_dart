void main() {
  final idade = 18;
  if (idade > 18) {
    print(
        'Você ja passou da idade de tirar carta de motorista. Mas ainda da tempo!');
  } else if (idade == 18) {
    print('Você está na idade de tirar carta de motorista.');
  } else {
    print('Você ainda não pode tirar carta de motorista.');
  }
}
